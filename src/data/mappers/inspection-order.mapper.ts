import { InspectionOrder } from "@/src/domain/entities/inspection-order.entity";
import { InspectionOrderModel } from "../models/inspection-order.model";

export class InspectionOrderMapper {
    static toDomain(model: InspectionOrderModel): InspectionOrder {
        return {
            id: model.id,
            number: model.number,
            status: model.status,
            priority: model.priority,
            asset_id: model.asset_id,
            asset_name: model.asset_name,
            asset_category: model.asset_category,
            asset_type: model.asset_type,
            planned_date: model.planned_date,
            started_at: model.started_at,
            finished_at: model.finished_at,
            actual_price: model.actual_price,
            total_work_minutes: model.total_work_minutes,
            inspector_id: model.inspector_id,
            inspector_name: model.inspector_name,
            created_by_name: model.created_by_name,
            estimated_man_hours: model.estimated_man_hours,
            notes: model.notes,
            estimate_service_price: model.estimate_service_price,
            estimate_part_price: model.estimate_part_price,
            grand_total: model.grand_total,
            created_at: model.created_at,
            updated_at: model.updated_at,
            attachments: model.attachments,
            order_items: model.order_items.map(item => ({
                id: item.id,
                status: item.status,
                type: item.type,
                program_id: item.program_id,
                issue_id: item.issue_id,
                attachments: item.attachments.map(att => ({
                    id: att.id,
                    entityId: att.entityId,
                    entityType: att.entityType,
                    originalName: att.originalName,
                    filePath: att.filePath,
                    mimeType: att.mimeType,
                    fileSize: att.fileSize,
                    uploadedBy: att.uploadedBy,
                    uploadedAt: att.uploadedAt,
                    deletedAt: att.deletedAt,
                    url: att.url,
                    status: att.status,
                })),
                details: {
                    name: item.details.name,
                    description: item.details.description,
                    tasks: item.details.tasks,
                    hour_price: item.details.hour_price,
                    man_hours: item.details.man_hours,
                    total_item_price: item.details.total_item_price,
                },
            })),
            order_components: model.order_components.map(comp => ({
                id: comp.id,
                inspection_order_id: comp.inspection_order_id,
                component_id: comp.component_id,
                component_name: comp.component_name,
                component_number: comp.component_number,
                type: comp.type,
                uom: comp.uom,
                fullfillment_source: comp.fullfillment_source,
                is_fullfilled: comp.is_fullfilled,
                quantity_required: comp.quantity_required,
                estimated_unit_price: comp.estimated_unit_price,
                estimated_total_price: comp.estimated_total_price,
                component_detail: {
                    id: comp.component_detail.id,
                    partNumber: comp.component_detail.partNumber,
                    name: comp.component_detail.name,
                    description: comp.component_detail.description,
                    type: comp.component_detail.type,
                    status: comp.component_detail.status,
                    uomId: comp.component_detail.uomId,
                    manufacturerId: comp.component_detail.manufacturerId,
                    minStock: comp.component_detail.minStock,
                    maxStock: comp.component_detail.maxStock,
                    defaultPurchasePrice: comp.component_detail.defaultPurchasePrice,
                    createdAt: comp.component_detail.createdAt,
                    updatedAt: comp.component_detail.updatedAt,
                    serializedComponents: comp.component_detail.serializedComponents.map(sc => ({
                        id: sc.id,
                        componentId: sc.componentId,
                        serialNumber: sc.serialNumber,
                        stock: sc.stock,
                        condition: sc.condition,
                        price: sc.price,
                        createdAt: sc.createdAt,
                        updatedAt: sc.updatedAt,
                    })),
                    nonSerializedComponents: comp.component_detail.nonSerializedComponents,
                    uom: comp.component_detail.uom ? {
                        id: comp.component_detail.uom.id,
                        name: comp.component_detail.uom.name,
                        code: comp.component_detail.uom.code,
                        createdAt: comp.component_detail.uom.createdAt,
                        updatedAt: comp.component_detail.uom.updatedAt,
                    } : undefined,
                },
            })),
            findings: model.findings.map(finding => ({
                id: finding.id,
                title: finding.title,
                description: finding.description,
                status: finding.status,
                reported_by: finding.reported_by,
                attachments: finding.attachments.map(att => ({
                    id: att.id,
                    entityId: att.entityId,
                    entityType: att.entityType,
                    originalName: att.originalName,
                    filePath: att.filePath,
                    mimeType: att.mimeType,
                    fileSize: att.fileSize,
                    uploadedBy: att.uploadedBy,
                    uploadedAt: att.uploadedAt,
                    deletedAt: att.deletedAt,
                    url: att.url,
                    status: att.status,
                })),
            })),
            logs: model.logs.map(log => ({
                id: log.id,
                status: log.status,
                reason: log.reason,
                created_at: log.created_at,
                user_name: log.user_name,
            })),
        };
    }
}